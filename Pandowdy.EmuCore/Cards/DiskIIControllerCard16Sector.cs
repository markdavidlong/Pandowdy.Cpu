using Pandowdy.EmuCore.DataTypes;
using Pandowdy.EmuCore.Interfaces;
using Pandowdy.EmuCore.Services;

namespace Pandowdy.EmuCore.Cards;

/// <summary>
/// Disk II Controller Card with 16-sector ROM (P5A/P6A ROM).
/// </summary>
/// <remarks>
/// <para>
/// This is the standard Disk II controller ROM used for DOS 3.3 and ProDOS, supporting
/// 16 sectors per track with 6-2 encoding.
/// </para>
/// <para>
/// The ROM contains the boot code that loads the first sector from track 0 into memory
/// and transfers control to the loaded code.
/// </para>
/// </remarks>
/// <remarks>
/// Initializes a new instance of the <see cref="DiskIIControllerCard16Sector"/> class.
/// </remarks>
/// <param name="cpuClocking">The CPU clocking counters for timing operations.</param>
/// <param name="factory">Factory for creating drive instances.</param>
/// <param name="statusMutator">Status mutator for publishing controller state changes.</param>
/// <param name="responseEmitter">Card response emitter for publishing card identification responses.</param>
public class DiskIIControllerCard16Sector(
    CpuClockingCounters cpuClocking,
    IDiskIIFactory factory,
    IDiskStatusMutator statusMutator,
    ICardResponseEmitter responseEmitter) : DiskIIControllerCard(cpuClocking, factory, statusMutator, responseEmitter)
{
    private static readonly byte[] Rom =
    [
        0xA2, 0x20, 0xA0, 0x00, 0xA2, 0x03, 0x86, 0x3C, 0x8A, 0x0A, 0x24, 0x3C, 0xF0, 0x10, 0x05, 0x3C, // $C600
        0x49, 0xFF, 0x29, 0x7E, 0xB0, 0x08, 0x4A, 0xD0, 0xFB, 0x98, 0x9D, 0x56, 0x03, 0xC8, 0xE8, 0x10, // $C610
        0xE5, 0x20, 0x58, 0xFF, 0xBA, 0xBD, 0x00, 0x01, 0x0A, 0x0A, 0x0A, 0x0A, 0x85, 0x2B, 0xAA, 0xBD, // $C620
        0x8E, 0xC0, 0xBD, 0x8C, 0xC0, 0xBD, 0x8A, 0xC0, 0xBD, 0x89, 0xC0, 0xA0, 0x50, 0xBD, 0x80, 0xC0, // $C630
