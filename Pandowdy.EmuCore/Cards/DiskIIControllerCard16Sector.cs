    ];

    /// <inheritdoc />
    public override string Name => "Disk II";

    /// <inheritdoc />
    public override string Description => "Disk II Controller - 16-Sector ROM";

    /// <inheritdoc />
    public override int Id => 10;

    /// <inheritdoc />
    public override byte? ReadRom(byte offset)
    {
        return offset < Rom.Length ? Rom[offset] : null;
    }

    /// <inheritdoc />
    public override ICard Clone()
    {
        return new DiskIIControllerCard16Sector(_clocking, _diskIIFactory, _statusMutator, _responseEmitter);
    }
}
